    success_count = 0
    failure_count = 0
    
    for idx, data in enumerate(MOCK_DATA, 1):
        req_body = json.dumps(data).encode("utf-8")
        req = urllib.request.Request(URL, data=req_body, headers=HEADERS, method="POST")
        
        try:
            with urllib.request.urlopen(req) as response:
                status_code = response.getcode()
                if status_code in (200, 201):
                    print(f"[{idx}/40] ✅ 성공: [{data['category']}] '{data['title'][:18]}...' (Status: {status_code})")
                    success_count += 1
                else:
                    print(f"[{idx}/40] ⚠️ 경고: '{data['title']}' 전송 완료했으나 예상치 못한 응답 코드: {status_code}")
                    failure_count += 1
        except Exception as e:
            print(f"[{idx}/40] ❌ 실패: '{data['title'][:18]}...' - 에러 발생: {e}")
            failure_count += 1
            
    print("=" * 60)
